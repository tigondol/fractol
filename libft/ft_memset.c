#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = (char*)s;
	while (i < len)
	{
		tmp[i] = c;
		i++;
	}
	return (s);
}

#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	*src;

	if (s)
	{
		i = 0;
		src = (char*)s;
		while (src[i])
		{
			write(1, &src[i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}

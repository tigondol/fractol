#include "libft.h"

int		ft_findwords(const char *s, int i, int wc, char c)
{
	if (s[i] >= 32 && s[i] <= 126 && s[i] != c)
		wc++;
	i++;
	while (s[i] != '\0')
	{
		while (s[i] == c)
			i++;
		if ((s[i] >= 32 && s[i] <= 126) && (s[i - 1] == c)
				&& s[i] != c)
			wc++;
		i++;
	}
	return (wc);
}
